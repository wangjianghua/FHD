uint16 mb_crc16(const uint8 ucFrame[], int32 iLen)
{
    uint8	ucCRCHi = 0xFF;
    uint8	ucCRCLo = 0xFF;
    int32	iIndex;
	int32	i = 0;

    while( iLen-- )
    {
        iIndex = ucCRCLo ^ ucFrame[i ++];
        ucCRCLo = ( uint8 )( ucCRCHi ^ aucCRCHi[iIndex] );
        ucCRCHi = aucCRCLo[iIndex];
    }
    return ( uint16 )( ucCRCHi << 8 | ucCRCLo );
}

uint16 mb_swap(uint16 usData)
{
	uint8	ucData[2] = { 0 };
	
	ucData[1] = (usData >> 8) & 0xFF;
	ucData[0] = usData & 0xFF;
		
	return ( uint16 )( ucData[0] << 8 | ucData[1] );;
}

int32 mb_double_to_byte(uint8 ucData[], fp64 fpData)
{
	uint8	*pData;
	
	pData = (uint8 *)&fpData;
	// Modbus数据按照Big indian模式，高字节在前，低字节在后
	ucData[7] = pData[0];
	ucData[6] = pData[1];
	ucData[5] = pData[2];
	ucData[4] = pData[3];
	ucData[3] = pData[4];
	ucData[2] = pData[5];
	ucData[1] = pData[6];
	ucData[0] = pData[7];
	
	return 8;
}


