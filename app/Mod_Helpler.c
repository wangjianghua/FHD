int32 mb_reserve_to_byte(uint8 ucData[], int32 num)
{
	int32	i;

	// Modbus数据按照Big indian模式，高字节在前，低字节在后
	for(i = 0; i < num; i ++) {
		ucData[2 * i + 1] = 0;
		ucData[2 * i + 0] = 0;
	}
	
	return 2 * num;
}

int32 mb_byte_to_reserve(const uint8 ucData[], int32 num)
{
	// Modbus数据按照Big indian模式，高字节在前，低字节在后
	return 2 * num;
}


uint8 iec_checksum(const uint8 ucFrame[], int32 iLen)
{
	int32	i;
	int32	checksum = 0;
	
	for(i = 0; i < iLen; i ++)
	{
		checksum = (checksum + ucFrame[i]) & 0xFF;
	}
	
	return checksum;
}


